#disserque uma variável
var=input('Digite um nome para verificar')

print('Tipo:',type(var))
print('É numerico?',var.isnumeric())
print('É alfanumerico?', var.isalnum())
print('É um espaço?',var.isspace())
print('É alfabetico?',var.isalpha())
print('É maiuculo?',var.isupper())
print('É minusculo',var.islower())
print('É decimal?',var.isdecimal())
